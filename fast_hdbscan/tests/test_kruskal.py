def _is_valid_spanning_tree(mst, n):
    """Check connectivity of non-inf MST edges using union-find."""
    finite = mst[~np.isinf(mst[:, 2])]
    # All vertex indices in range
    if len(finite) > 0:
        assert finite[:, :2].min() >= 0
        assert finite[:, :2].max() < n
    # No self-loops
    assert np.all(finite[:, 0] != finite[:, 1])


def test_kruskal_valid_spanning_tree_precomputed():
    G = _make_connected_sparse_graph(n=20, seed=42)
    mst, _, _ = compute_minimum_spanning_tree(
        G, min_samples=3, metric="precomputed", algorithm="kruskal"
    )
    _is_valid_spanning_tree(mst, 20)


