@pytest.mark.parametrize("dataset,X,G", [
    ("blobs", _X_blobs, _G_blobs),
    ("moons", _X_moons, _G_moons),
])
@pytest.mark.parametrize("min_samples", [1, 5])
def test_hdbscan_kruskal_boruvka_precomputed_label_parity(dataset, X, G, min_samples):
    """
    HDBSCAN with algorithm='kruskal' on precomputed graph must yield the same
    cluster assignments as algorithm='boruvka'.
    """
    k = HDBSCAN(
        min_cluster_size=10, min_samples=min_samples,
        metric="precomputed", algorithm="kruskal",
    ).fit(G)
    b = HDBSCAN(
        min_cluster_size=10, min_samples=min_samples,
        metric="precomputed", algorithm="boruvka",
    ).fit(G)

    k_n = len(set(k.labels_) - {-1})
    b_n = len(set(b.labels_) - {-1})
    assert k_n == b_n, (
        f"[{dataset} ms={min_samples}] cluster count: kruskal={k_n} boruvka={b_n}"
    )

    aligned = _align_labels(b.labels_, k.labels_)
    assert np.array_equal(b.labels_, aligned), (
        f"[{dataset} ms={min_samples}] label mismatch after alignment"
    )


