def _is_valid_spanning_tree(mst, n):
    """Check connectivity of non-inf MST edges using union-find."""
    finite = mst[~np.isinf(mst[:, 2])]
    # All vertex indices in range
    if len(finite) > 0:
        assert finite[:, :2].min() >= 0
        assert finite[:, :2].max() < n
    # No self-loops
    assert np.all(finite[:, 0] != finite[:, 1])


