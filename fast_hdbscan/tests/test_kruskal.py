# 3. Kruskal vs Borůvka MST weight parity on precomputed sparse graphs
# ---------------------------------------------------------------------------

@pytest.mark.parametrize("min_samples", [1, 3, 5])
def test_kruskal_boruvka_precomputed_mst_weights(min_samples):
    """
    Kruskal and Borůvka, given the same precomputed graph, must produce
    identical MST edge-weight multisets.

    Kruskal works entirely in float64; Borůvka uses float32 internally but
    patches weights back to float64 afterwards.  On a full pairwise distance
    graph (no float32 precision gap) the resulting sorted weights must be
    bit-for-bit equal.
    """
    G = _G_blobs

    mst_k, _, cd_k = compute_minimum_spanning_tree(
        G, min_samples=min_samples, metric="precomputed", algorithm="kruskal"
    )
    mst_b, _, cd_b = compute_minimum_spanning_tree(
        G, min_samples=min_samples, metric="precomputed", algorithm="boruvka"
    )

    assert mst_k.shape == mst_b.shape, "MST sizes must match"
    np.testing.assert_array_equal(
        np.sort(mst_k[:, 2]),
        np.sort(mst_b[:, 2]),
        err_msg=(
            f"Kruskal vs Borůvka sorted MST weights differ "
            f"(min_samples={min_samples})"
        ),
    )
    np.testing.assert_array_equal(cd_k, cd_b, err_msg="Core distances must match")


