@pytest.mark.parametrize("min_samples", [1, 5])
def test_kruskal_brute_vs_boruvka_mst_weights(min_samples):
    """
    Brute-force Kruskal (exact MST) and Boruvka (also exact) on the same
    feature data must produce identical sorted MST weight multisets.

    Small precision differences are possible because Boruvka works in
    float32 rdist internally while brute Kruskal uses cdist float64.
    We check approximate equality with tight tolerance.
    """
    mst_k, _, cd_k = compute_minimum_spanning_tree(
        _X_blobs, min_samples=min_samples, metric="euclidean",
        algorithm="kruskal", knn_k=None,
    )
    mst_b, _, cd_b = compute_minimum_spanning_tree(
        _X_blobs, min_samples=min_samples, metric="euclidean",
        algorithm="boruvka",
    )
    assert mst_k.shape == mst_b.shape
    np.testing.assert_allclose(
        np.sort(mst_k[:, 2]),
        np.sort(mst_b[:, 2]),
        rtol=1e-5,
        err_msg=f"Brute Kruskal vs Boruvka MST weights differ (ms={min_samples})",
    )


