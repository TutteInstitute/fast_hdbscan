def test_kruskal_known_triangle():
    """
    3-node complete graph.  Edges: 0-1 w=1, 1-2 w=2, 0-2 w=3.
    MST must contain 0-1 (w=1) and 1-2 (w=2).  Total weight = 3.
    """
    rows = [0, 1, 1, 2, 0, 2]
    cols = [1, 0, 2, 1, 2, 0]
    data = [1.0, 1.0, 2.0, 2.0, 3.0, 3.0]
    G = sparse.csr_matrix((data, (rows, cols)), shape=(3, 3), dtype=np.float64)

    mst, _, core_dists = compute_minimum_spanning_tree(
        G, min_samples=1, metric="precomputed", algorithm="kruskal"
    )
    assert mst.shape == (2, 3), "MST must have n-1=2 edges"
    assert np.isclose(np.sort(mst[:, 2]).sum(), 3.0), "Total MST weight must equal 3"


